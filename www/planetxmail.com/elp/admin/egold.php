			<h3>E-gold setup</h3>
			Log into your e-gold account and goto <b>account info</b> and	set your <b>Alternate Pass Phrase</b>.
			<br><br>
			Do not mistake this for your regular pass phrase. ( Which is just your password to log in. )
			<br><br>
			Then, set the same Alternate Pass Phrase in your <b>ELP Configurations</b>.
			<br><br>
			Finally, set your e-gold account number in ELP Config.
			<br>
			<br>
			The Alternate Pass Phrase is used for transaction verifing purposes and is <b>required</b> for
			Elite Scripts to process any orders using an e-gold account.
