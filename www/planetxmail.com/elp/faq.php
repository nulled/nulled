      <a name="error"><b>Why does ELP crash while mailing lots of SafeLists?</b></a>&nbsp;&nbsp;<a href="#toc">Back to TOC</a><br><br>
      When large amounts of SafeLists are Queued for mailing more Memory from your computer is needed.  Also other programs
      that are running on your Desktop may interfer with the operation of ELP.  One option is to purchase MORE RAM for your
      computer.  If you have 128 megs or less you may need to buy more ram.  Or, try doing the following to Maximize your
      system Memory:
      <ul>
        <li>Close ALL programs running except, of course, ELP.</li>
        <li>Remove ANY user / password Auto Form Fill programs that are running.</li>
        <li>If your task bar has 10 or more icons ( programs ) you may need to cut back and remove them, at least
        temporarilty while ELP is running.  Simply right click each Icon that is not important and it should have a
        disable or remove from system tray options.</li>
        <li>Disable or close any virus scanners that are running while you plan an ELP session.</li>
        <li>Disable or close any FireWall software you may have.</li>
      </ul>
      Basically, your trying to remove all programs that are running, while ELP is running.  This will greatly improve
      performance.
