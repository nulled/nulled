  <font size="+1"><b>List Types</b></font>
  <p>
  There are essentially two types of lists we offer.
  </p>

  <ul>
    <li>SafeLists</li>
    <li>NewsLetters</li>
  </ul>

  <b>SafeLists</b>
  <p>
  SafeLists are a collection of voluntarily subscribed user e-mail addresses that wish to receive information on the Safe List's subject.
  Typically used in the marketing of items or services.  Each member that signs up to the Safe List can then receive  mail from all other
  members of that list, in return, the member can send mail to all members of the list.  Since, each e-mail  is filtered, actively used
  and voluntarily supplied by the e-mail address owner, this makes for good non-spam e-mail based advertising that may potentially reach
  thousands of readers. SafeLists also control the emailing rates per member by restricting how many times a member can mail per week. Also,
  a specialty algorithm is used to calculate the optimal percentage of members mailed to prevent INBOX over saturation. A Credit System is
  also used to encourage member's to read each others emails.
  </p>
  <b>NewsLetters</b>
  <p>
  A NewsLetter is a collection of e-mail addresses that were obtained by people signing up to receive information on what the NewsLetter
  was promoting.  They are just a collection of valid e-mail addresses, except only the NewsLetter <i>owner</i>  can e-mail to all the
  members of the NewsLetter.  This is useful for organizations that collect e-mail addresses from customers that wish to remind, advise
  or sell newly released products to them. We offer <b>HTML</b> and Plain-text based NewsLetter capabilities!
  </p>
  <p>
  Another form of NewsLetter is for internal usage within a company to advise, remind and keep informed their employees on information they
  should or need to know about.  Much like an intercom system but in this case is e-mail based.
  </p>
  <p>
  Depending on your demands and situation either of the three should fit the description of a type of list you may need or desire.
