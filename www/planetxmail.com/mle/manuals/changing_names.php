			<h2>Changing your List Name and List Owner User Name</h2>
			<i>Main Options->List Name</i>
			<br>and<br>
			<i>Main Options->List Owner User Name</i>
			<br><br>
			Very strait forward.  Choose: <i>Main Options->List Name</i> and enter in your new list name.
			<br><br>
			Or
			<br><br>
			Choose: <i>Main Options->List Owner User Name</i> and enter in your new List Owner User Name.
			When you change your Owner User Name please take careful note of this!  Next time you try to Log into
			your admin control panel, you will need to enter this name and not the previous one!
			<br><br>
			Changing the List Name and/or List Owner User Name will not effect the operation of your List.
			<br><br>
			SPECIAL NOTE: Changing the list name WILL NOT change the name entered for your FROM ADDRESS in all members emails.
			<br>
			( i.e.  FROM: <b>My_LIST</b> &lt;whatever@where.com&gt; ) In order to change the <b>My_List</b> part goto <i>Main Options->List Addresses</i>
