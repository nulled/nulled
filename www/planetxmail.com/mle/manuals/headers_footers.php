			<h2>Using the Headers, Footers and System Messages</h2>
			<i>Main Options->Footers/Headers</i>
			<br><br>
			There are five ( 5 ) message blocks you can edit that are used by the system and in out going email ads:
			<ul>
				<li>Email Header</li>
				<li>Email Footer</li>
				<li>Subscribe Confirmation</li>
				<li>Subscribe Success</li>
				<li>Unsubscribe Success</li>
			</ul>
			<h3>Email Header</h3>
			All out going emails MUST have a header and a footer.  Typically the HEADER tells the reading what list the AD came from
			with additional information.  You as List Owner can also take advantage of this space and place ADs.  You can even SELL
			AD space to members that wish to buy from you.  Just another way you can make alittle extra money.
			<h3>Email Footer</h3>
			All out going mail MUST contain an email footer.  The most important part of the foot is the removal link.  All Optin
			based emails MUST contain a working remove link by federal and state laws, or it will be concidered SPAM email.
			Planet X Mail mail systems ensure that the unsubscribe link be present or it will not allow a footer to be saved to
			the database. <b>[unsubscribe_link]</b> is MANDATORY and MUST be present in the footer.  The system will of course
			replace this [tag] with a remove link individually assigned to each member.  Each member will have a different remove
			link based on their userID and a specially encrypted hash code to ensure no one can remove members from lists illegally.
			<h3>Subscribe Confirmation</h3>
			This message is mailed to all members that wish to sign up to your SafeList.  Again you can place what ever you want in here
			including ADs but make sure you have <b>[subscribe_link]</b> tag in there so people can use this link to comfirm they
			do want to subscribe to your list.  All new sign ups receive an individually constructed sign up link to prevent anyone
			from signing up people with out their approval.
			<h3>Subscribe Success</h3>
			Once the member has clicked on the subscribe link provided in the Subscribe Confirmation email, they will receive a
			Subscribe Success email which again, you can place your Ads and additional information about your list.  It is wise to
			include the <b>[login_link]</b> so members know where to access their newly Subscribed SafeList.
			<h3>Unsubscribe Success</h3>
			When a member clicks the removal link, they will get an email saying good bye and thank you for choosing your SafeList.
			Place what you want in this email, but make sure it states an email address to email you if they DID NOT choose to
			optin-OUT on their own and report possible fraud or abuse from an outside source that is trying to unsubscribe your
			members.  It is impossible to forge an unsubscribe link since each member gets their own
			unique userID and specially encrypted hash code.  In other words your Member data is safe.
