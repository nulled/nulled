			<h2>How to properly use the HOME PAGE section of your SafeList</h2>
			<i>Main Options->Home Page</i>
			<br><br>
			When members first log into your SafeList after signup, they will see your Home Page for whatever status level
			that member is currently at.  You can place whatever
			HTML you want in here to create your own home page.  The home page could contain the following:
			<ul>
				<li>Information and Greeting to your members about your SafeList.</li>
				<li>Other Advertising such as banners and links to other affiliate programs you sell.</li>
				<li>Section where Free Members and Professional Members can upgrade to a higher status you offer.</li>
				<li>Any other information you want your members to see when they log into the SafeList.</li>
			</ul>
			Since, there are three ( 3 ) status levels members can be, Planet X Mail SafeLists allow you to individually edit
			three different Home Pages.  One home page for each Member Status level can be created!  Or, if you choose, all
			members can see the same Home Page. ( Simply cut and paste the HTML to all three HTML editting text areas to make
			them all identical. )
			<br><br>
			However, most List owners would like each status level to see a different Home Page.  Knowing HTML is a must in this
			case.  HTML is a mark up language ( coding language ) that creates web pages.  On this page I will show you some
			basic HTML "tags" that you can use to create various text effects.  It is recommended you go here
			<a href="http://www.w3schools.com/html/default.asp" target="_BLANK">www.w3schools.com</a> to learn more indepth HTML.
