			<h2>Using the List Profiler</h2>
			<i>Main Options->List Profiler</i>
			<br><br>
			The List Profiler is used to allow your SafeList to be displayed on Planet X Mail's high traffic website!
			The Profile consists of 4 things:
			<ol>
				<li>Title Image/Logo</li>
				<li>Link to your Sales Pages.</li>
				<li>Description of your SafeList and what you have to offer.</li>
				<li>Small Testimonial about Planet X Mail's service.</li>
			</ol>
			Simply fill out the List Profiler form and submit it.  We will then review your data and place what you submitted
			onto our <a href="http://www.planetxmail.com/index.php?option=clients" target="_BLANK">Clients Area</a>.
			<br><br>
			It typically takes 1-3 days for us to verify your submittion.  There are two ways to tell if your submittion is currently being
			displayed.
			<ol>
				<li>You will see <b>verified</b> at the top of the List Profiler form.</li>
				<li>Check the <a href="http://www.planetxmail.com/index.php?option=clients" target="_BLANK">Clients Area</a> for your submittion.</li>
			</ol>
			If you edit your submittion after submitting once before your Verified will then be set to Unverified and we will go through the
			Verifing process over again each time you make a change to your Profile.
