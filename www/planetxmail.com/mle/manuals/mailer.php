			<h2>Using the Admin Mailer</h2>
			<i>Main Options->Admin Mailer</i>
			<br><br>
			The Admin mailer is a powerful tool.  You can use it to mail notices to all your members, vai their
			contact or list address.
			<br><br>
			You can also mail your own Advertisements and offerings using this Mailer.  This tool will allow
			you to mail to your list ONE ( 1 ) time per day.  We place this restriction as to prevent abuse and over mailing.
			Over mailing in this case would be no different from Spamming them.
			<br><br>
			To use the Mailer simply type in your Subject and Message then click and Blue, Save Message button.  You must save your
			message before you can mail it.  It will save the message for you to edit or remail at a later date.
			<br><br>
			WARNING!  If you
			are planning on typing a very long Message and you try to save the message after 30 minutes of idle, your Admin Log in
			session will time out and you will lose all your type!  It is suggested that you type your long thought out message in
			Notepad on your computer THAN copy and paste the letter to the Admin Mailer Message area and save it!
			<br><br>
			Once your message is saved and ready for mailing click on the 3 status levels you want to mail to ( Free, Pro or Executive )
			than choose to mail to their contact or list address
			and click the Send Mail button.  If our mail servers are not bogged down it will mail your letter quickly.  If you
			are trying to mail during peak business hours ( 11am - 4pm Pacific time ) than you may have to wait.  It will tell you
			how large the waiting time is.
