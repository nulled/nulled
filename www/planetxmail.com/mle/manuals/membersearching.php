			<h2>Member Searching and Management</h2>
			On the Main Admin Page you will see in the blue colored area the ability to search members by:
			<ul>
				<li>User Name</li>
				<li>Contact Address</li>
				<li>List Address</li>
			  <li>First Name</li>
			  <li>Last Name</li>
			</ul>
			You must enter at least THREE (3) characters to do a search.  It will then reveal to you all the matches it found.
			Sometimes dozens of matches.  When multiple matches are found it will display additional information about each
			match like first and last name to better narrow down your search.
			<br><br>
			Once you find your match, just click on the link for the match and it will load the Detailed Profile View for that
			member.
			<br><br>
			Once in the Detailed Profile View you can observe many things about the current state of the member like:
			<ul>
				<li>First, Last and User Name</li>
				<li>Date Signed up</li>
				<li>Contact and List Addresses set</li>
				<li>Status Level</li>
				<li>Vacation Status</li>
				<li>Number of URL Trackers they created.</li>
				<li>Number of Days since they Last Logged in. After 180 days will be tagged as a Lame account.</li>
			</ul>
			The things you can edit on this page for the Member are:
			<ul>
				<li>Status Level ( Free, Professional, Executive)</li>
				<li>Change Password</li>
				<li>Delete the member outright.</li>
			</ul>
			You can not edit their Contact or List address because we keep all email addresses OPTIN BASED only.  They will have
			to change their email addresses themselves by going into their Member profile after logging into their account and using
			the confirmation OPTIN email system to make the changes.
			<br><br>
			When you change the status level of a member, that member must log out then log back in for the changes to take place.
			<br><br>
			Note: A member that has not logged in after 180 days will be deleted my the system, automatically.  It is concidered
			a lame account.  Most lame accounts do not have a list address set and also contain a dead or bouncing Contact address.
			We do mail the accounts contact address several times at different intervals through out the 180 days trying to notifying the
			account owner of possible deletion.  We give PLENTY of notices and time for them to save their accounts, if they so
			choose.
			<br><br>
			As a result of our, lame account deletion system, you may see your list go down in member count.  This is because
			people are abondoning the SafeList account more then people are signing up.  This is not nessarily a BAD thing as
			having a nice clean members list is better than keeping around dead accounts there by freeing up resources otherwise
			wasted in mailing to these lame accounts.
