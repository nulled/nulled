			<h2>Setting up your List Payment Options</h2>
			<i>Main Options->List Wizard</i>
			<br><br>
			The best way to setup your Mailing List for payments is to use our <b>List Wizard</b>.  You can access the Wizard from
			<i>Main Options->List Wizard</i> from the Admin Control Panel.
			<br><br>
			This Wizard will ask you a few questions.  Near the bottom of the List Wizard form you will see two ( 2 ) options regarding
			List Payment Configuration, selectable using the radio buttons:
			<ul>
				<li>Yes, use the SafeLists Built-in Upgrade pay form system.</li>
				<li>No, use pay forms on your own servers.</li>
			</ul>
			<h3>Yes, use the SafeLists Built-in Upgrade pay form system.</h3>
			If you choose this option ( recommended ), all you have to do is fill in your Merchant account information.  The rest will
			be taken care of for you.  If you do not have some of the Merchant accounts specified, simply leave their entries BLANK
			and the system will ignore them. <i>For instance, if you do not have a ClickBank account do not fill in it's form information.</i>
			<br><br>
			Also, when you use our built-in method mentioned here, it should auto-upgrade the members for you, that pay.  One thing
			to note about this is that after they pay they MUST click the Continue to YOUR SALES PAGES link in order to complete the
			order and auto-upgrade them.  Sometimes people do not do this and so you may need to manually upgrade them yourself.
			( To change a member's status level, goto their Profile. )
			<br><br>
			If you are HTML savvy and know how to set up payforms yourself.  You can manually edit or add what the List Wizard
			created by going to <i>Main Options->Manually Edit PayLinks</i>  If you do not know what your doing in this section
			we recommend you not tamper with the code.  You can always re-submit the List Wizard to re-create
			the HTML it generated.
			<h3>No, use pay forms on your own servers.</h3>
			If you choose this option, all you have to enter are the Web pages ( URL/Link ) to the forms you set up yourself on your
			servers.  Note that since we can not track the members information once they leave our servers, the auto-upgrading will not
			occur.
