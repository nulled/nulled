			<h2>Uploading Title Graphic, Banners and Bill Boards</h2>
			Planet X Mail SafeLists are designed for net-marketing in mind.  It is NOT just a Mailer.  You can do the following:
			<ul>
				<li>Upload an unlimited amount of standard or smaller sized banners.</li>
				<li>Create an unlimited amount of Bill Board ADs.</li>
				<li>Upload your own custom Title graphic/business logo.</li>
			</ul>
			There are three ( 3 ) sections we will talk about:
			<ol>
				<li>Upload Title Graphic</li>
				<li>Upload Bill Boards/Banners to the "Seen After Sending an AD" AD Collage.</li>
				<li>Upload Standard sized banners that are seen through out the SafeList. Banner Rotation</li>
			</ol>
			<h3>Upload Title Graphic</h3>
			It is recommended you upload your own Title graphic.  This makes your Mailing List look more unique.  If you do not
			upload your own the system will provide a default one saying, "Multi-List Enterprise".  Goto <i>Main Optins->Title Graphic</i>
			to upload your title.
			<br>
			<h3>Seen After Sending an AD</h3>
			A <font color="#99CCCC"><b>teal</b></font> colored section on the Main Admin page contains two ( 2 ) sections. One of them is called <b>Seen After Sending an AD</b>.
			Here you can create your "collage" of Bill Board and Banner images.
			These Bill Board Ads and Banners are only displayed AFTER a member mails out, regardless if they reached their daily/weekly mailing
			limit or not.  All Bill Board ADs and Banners uploaded will all be displayed in alternating orderto create a "collage" of
			ADs for the viewer to see and hopefully	click on.
			<br><br>
			Bill Board ads are unique to Planet X Mail SafeLists.  They contain four ( 4 ) parts.
			<ol>
				<li>Subject Line</li>
				<li>Ad message body</li>
				<li>Small Logo/Image</li>
				<li>URL/Link where the viewer will be taken to.</li>
			</ol>
			<br>
			<h3>Banner Rotation</h3>
			The second section in the <font color="#99CCCC"><b>teal</b></font> colored area is: <b>Banner Rotation</b>.  Here you can upload Standard sized banners and place
			a URL/Link on them for redirecting visitors.  These banners are randomly picked and loaded on all Miscelaneous pages like: Subscribe
			Success, URL tracker results, Confirmation Screens and others.
			<br><br>
			<b>Special Tips</b>:  To utilize these two ( 2 ) sections to their greatest potential, we recommend you sell this AD SPACE to your members!
			You can charge whatever fees you want for uploading their Banners and creating custom Bill Boards!  As you can see using the
			<b>Seen After Sending an AD</b> and <b>Banner Rotation</b> sections can be used for your OWN ADs or selling AD space to others
			that are looking to advertise to your traffic.
