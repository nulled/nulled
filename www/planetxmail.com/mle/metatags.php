      <h2>Make your WebPage "Search Engine Friendly"</h2>
      </center>
			You may have seen those, "Search Engine Submitters" all over the net.  Well, frankly you do not need them!
			The fact of the matter is, Search Engines "spider" the internet on their own.  Spidering means that a Search Engine
			like, yahoo.com, regularly searchs the internet for new web sites and changes in websites. It then adds the collected,
			"spidered", data on it's own!
			<br><br>
			You have no control whether or not your web site gets "spidered" or not.  In fact, its a GOOD thing that is does, because
			Search Engines will list your web site FOR YOU! So, all this hype about "Search Engine Submitters" is all false.  The way
			yahoo.com can find out your web site is there, even though you may not have thought you told anyone, is because it has access
			to the domains list when you Registered for your domain!
			<br><br>
			So, just give it a few weeks, the search engines WILL spider your site and add it to its search engine.  After all thats
			the JOB of search engines, to give us a way to look up any and all types of web sites.
			<br><br>
			One thing you can do to be prepared when your web site gets "spidered" is to set up what is called, <b>META TAGS</b>.
			These META TAGS should be in your INDEX file, or first page that is loaded when you access your site.  In your index file
			META TAGs go into your HEAD section of your index file.
			<br><br>
			So when say, yahoo.com, comes to "spider" your site it looks for META TAGS to aid it in categorizing your site and establishing
			KEY WORDS to aid people in finding your site, when they do an actual search.
			<br><br>
			<pre>
      &lt;html&gt;
      &lt;head&gt;
      &lt;title&gt;My WebSite Index File&lt;/title&gt;
      &lt;META NAME="description" CONTENT="Tutorial how to use meta tags"&gt;
      &lt;META NAME="keywords" CONTENT="internet technology marketing"&gt;
      &lt;/head&gt;
      &lt;body&gt;
      &lt;table&gt;
      ... etc.
			</pre>
			Above you will notice the META TAGs are placed in the HEAD section of your HTML page.  This is REQUIRED.  Now when a Search Engine
			comes to visit and analyse your site, it will see these META TAGs and know what to do.  The <b>name="description"</b> will help
			the search engine in reinforcing YOUR terminology instead of the search engine finding one itself. The <b>name="keywords"</b>
			will help the search engine in letting it know what key words it should focus on when users try doing a search.  Try your best to
			describe your site thinking about what a Search Engine <i>User</i> may type in, in regards to finding a site like yours.
			<br><br>
			Keep in mind that META TAGs may sound great but, they are not all support by search engines.  The Search Engine still makes a lot
			of calls on its own in trying to organize web sites and establishing key words to use.  But, it is not bad to have META TAGs in your
			HTML pages.
