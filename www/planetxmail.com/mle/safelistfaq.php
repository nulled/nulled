      <a name="missingmail"><b>6. Why don't I see my AD sent to my List Address for the SafeList I am mailing to?</b></a> - <a href="#toc">Back to TOC</a><br /><br />
      You should be getting your own AD to the list address set for that Safelist your mailing to.<br />
      <br />
      If you are not it could mean a number of things.<br />
      <br />
      1 - Your Email Inbox got full.<br />
      2 - Your Email intermittently bounces.<br />
      3 - You are looking in the wrong Email box for what you are expecting.<br />
      <br />
      Our systems mail all emails all the time.  We are not responsible for the RECEIVING Mail Servers reliablity.  The Email is sent to the server once and we receive a bounce Back Email saying why we couldn't connect to the server.  We anaylse all bounce emails and have a  system in place to extract exact data from them.<br />
      <br />
      There are literally 1000's that use our system.  And therefore if a system failure happens it more often effects EVERYONE and not just individuals.  Our server/database/mailer systems are very reliable!

