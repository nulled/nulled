      <a name="urltracker"><b>4. How do I make and use a URL Tracker?</b></a> - <a href="#toc">Back to TOC</a><br /><br />
      <i>First</i>, you need to enter a "Friendly Name" for the URL tracker.<br />
      <i>Next</i>, enter the URL/Link you wish to Track and click, Submit URL Tracker.<br />
      <i>Finally</i>, select the URL that is given, this is your URL Tracker, and replace the Plain "Original" URL with the URL Tracker.
      <br /><br />
      Now, when someone clicks on the URL Tracker they are taken to our server, we record the hit then immediately send them on their way
      to the actual link, which is your website, or the URL you link it to.  The person clicking the link will never tell it goes to our server first
      so is a very effective way to count the number of times an AD is viewed.

