      <a name="delete"><b>5. Why do I receive a Deletion Notice even after I logged into my account recently?</b></a> - <a href="#toc">Back to TOC</a><br /><br />
      If you continue to get deletion notices even after you log in, this means
      you have more than one account. Make sure that the deletion notice your getting matches the correct account you logged into! It may not
      be the same account you think!  The deletion Email has all the details like, List Name, User Name and Dates.

