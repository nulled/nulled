      <a name="turingkey"><b>2. What is the Turing Key for?</b></a> - <a href="#toc">Back to TOC</a><br /><br />
      The turing key is there to prevent other SafeList Submitters from accessing our SafeLists.  These SafeList Submitters could potentially
      be abused and over used and wish to allow only the SafeList Submitters we have control over to prevent abuse, if it should occur.  On the
      Sign Up form the Turing Key is to prevent automated Sign Up programs.

      <hr />
      <a name="turingkeyreload"><b>3. Why doesn't the Turing Key let me in?</b></a> - <a href="#toc">Back to TOC</a><br /><br />
      This may occur when your Web Browser "caches" the Log In page which now contains an OLD Turing key.  Simply hit F5 function key on your keyboard to
      refresh the Log In page and get a new Turing Key.
      <br />
      Also, the Turing Key has a limited life.  If you load the Log In page and walk away for more then a minute you will need to refresh the
      Log In page by hitting F5 function key on your key board to get a new Turing Key.

