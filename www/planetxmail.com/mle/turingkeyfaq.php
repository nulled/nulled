      <h2>What is a turing key?</h2>
      <p>
        A turing key is a randomly generated number.  It will be different every time you log in. You must read the jpeg file
        that has the 6 digit number and enter it in the turing key field in order to login.
        <br><br>
        To enter the turing key, simply look at the 6 digit number ( which is the funny looking grid with the number )
        and enter it in the text field under neither.  Then hit the login button.
      </p>
