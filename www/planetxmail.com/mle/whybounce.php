      <h2><center>Why bounces are bad.</center></h2>
      A bounced email is just an email that is generated if the mail box your trying to reach isnt available.  There are MANY
      reasons why a mail box might be <i>undeliverable</i>.  Some are listed below:
      <ol>
      	<li>Mail Box FULL - Over quota.</li>
      	<li>Server not found.</li>
      	<li>Server under too much load at time of delivery attempt.</li>
      	<li>Server has Black Listed our server.  Usually because they think we are Spamming.</li>
      </ol>
      <b>Note:</b> Reason #4 would be false since we DO NOT TOLERATE spam.
      <br><br>
			Our servers mail a lot of mail a day.  In fact over 2 Million per day, that we can not afford the luxury of placing failed
			delivery attempts back in the queue to try again.  If we did the mail queue would fill up beyond any hope of being able to
			keep up and deliver to those mail boxes that ARE deliverable.
			<br><br>
			To deal with this we have come up with a system that compromises with mail servers that are "sometimes there and sometimes not".
			We place bounced email addresses in a bounced list for a full 2 hours.  Meaning that our systems will not try to
			deliver mail to any email addresses on this list.  AFTER 2 hours from the LAST bounce email received we remove that email
			from the bounced list and allow it another chance for mailing.
			<br><br>
			In the future we hope to better improve our bounce handling system to be more efficient.  But, for now it is the very thing
			that has keep our Service as a List Host in operation and why you enjoy 99.9% uptimes!  So, we can't complain.
			<br><br>
			I hope this clears things up for you if you are bewildered as to why bounces occur and why it COULD be you. If your Black Listed try
			contacting your email host and tell them you are on a SafeList and not a SPAM List!  Also, for those that bounce sometimes
			and sometimes not, you have to be patient.
